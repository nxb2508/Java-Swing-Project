    public void showBtnClear() {
        String pin = String.valueOf(jpwfPin.getPassword());
        String currentPassword = String.valueOf(jpwfCurrentPassword.getPassword());
        String newPassword = String.valueOf(jpwfNewPassword.getPassword());
        String retypeNewPassword = String.valueOf(jpwfRetypeNewPassword.getPassword());
        if (!pin.equals("") || !currentPassword.equals("") || !newPassword.equals("") || !retypeNewPassword.equals("")) {
            btnClear.setEnabled(true);
        } else {
            btnClear.setEnabled(false);
        }
    }

