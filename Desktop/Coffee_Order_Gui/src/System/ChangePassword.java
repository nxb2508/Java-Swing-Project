    private void btnUpdatePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePasswordActionPerformed
        // TODO add your handling code here:
        String pin = String.valueOf(jpwfPin.getPassword());
        String currentPassword = String.valueOf(jpwfCurrentPassword.getPassword());
        String newPassword = String.valueOf(jpwfNewPassword.getPassword());
        String retypeNewPassword = String.valueOf(jpwfRetypeNewPassword.getPassword());
        UserDao userDao = new UserDao();
        User user = userDao.findByUsername(usernameLogin);
        if (!pin.equals(pinRegex)) {
            JOptionPane.showMessageDialog(null, "Ma PIN Khong Dung!");
            jpwfPin.setText("");
        } else if (!currentPassword.equals(user.getPassword())) {
            JOptionPane.showMessageDialog(null, "Mat Khau Cu Khong Dung!");
            jpwfCurrentPassword.setText("");
        } else if (!newPassword.matches(passwordRegex)) {
            JOptionPane.showMessageDialog(null, "Mat Khau Phai Chua 6-18 Ky Tu Hoac So!");
            jpwfNewPassword.setText("");
            jpwfRetypeNewPassword.setText("");
        } else if (!newPassword.equals(retypeNewPassword)) {
            JOptionPane.showMessageDialog(null, "Mat Khau Moi Khong Trung Khop!");
            jpwfNewPassword.setText("");
            jpwfRetypeNewPassword.setText("");
        } else {
            user.setPassword(newPassword);
            try {
                userDao.update(user);
                JOptionPane.showMessageDialog(null, "Thay Doi Mat Khau Thanh Cong!");
            } catch (SQLException ex) {
               
            }
        }
    }//GEN-LAST:event_btnUpdatePasswordActionPerformed

