    private void jtbBillMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbBillMouseClicked
        // TODO add your handling code here:
        int row = jtbBill.getSelectedRow();
        if(row >= 0){
            int confirm = JOptionPane.showConfirmDialog(null, "Chon", "Ban Co Muon Mo File Khong", JOptionPane.YES_NO_OPTION);
            if(confirm == 0){
                String fileBill = (String) jtbBill.getModel().getValueAt(row, 0);
                try {
                    OpenBillPdf.openFile(fileBill);
                } catch (IllegalArgumentException ex) {
                    JOptionPane.showMessageDialog(null, "File Khong Ton Tai!!!");
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, "Loi Mo File!!!");
                }
            }
        }
    }//GEN-LAST:event_jtbBillMouseClicked

