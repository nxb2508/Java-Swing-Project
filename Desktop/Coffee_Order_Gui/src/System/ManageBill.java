    public void showTableBill() throws SQLException {
        String customerName = jtfCustomerName.getText();
        String mobileNumber = jtfMobileNumber.getText();
        String orderDate = "";
        if (!jtfOrderDate.getText().equals("Format: yyyy-mm-dd")) {
            orderDate = jtfOrderDate.getText();
        }
        String orderTime = "";
        if (!jtfOrderTime.getText().equals("Format: hh:mm:ss")) {
            orderTime = jtfOrderTime.getText();
        }
        List<Bill> bills = new BillDao().getAllBill(customerName, mobileNumber, orderDate, orderTime);
        DefaultTableModel billModel = (DefaultTableModel) jtbBill.getModel();
        billModel.setRowCount(0);
        for (Bill temp : bills) {
            billModel.addRow(new Object[]{temp.getBillId(), temp.getCustomerName(), temp.getMobileNumber(), temp.getOrderDate(), temp.getOrderTime(), temp.getTotal()});
        }
    }
    
    public void showTableBillSort() throws SQLException {
        String customerName = jtfCustomerName.getText();
        String mobileNumber = jtfMobileNumber.getText();
        String orderDate = "";
        if (!jtfOrderDate.getText().equals("Format: yyyy-mm-dd")) {
            orderDate = jtfOrderDate.getText();
        }
        String orderTime = "";
        if (!jtfOrderTime.getText().equals("Format: hh:mm:ss")) {
            orderTime = jtfOrderTime.getText();
        }
        boolean dateTimeAsc = jrbDateTimeAsc.isSelected();
        boolean dateTimeDesc = jrbDateTimeDesc.isSelected();
        boolean totalAsc = jrbTotalAsc.isSelected();
        boolean totalDesc = jrbTotalDesc.isSelected();
        
        List<Bill> bills = new BillDao().getAllBill(customerName, mobileNumber,
                orderDate, orderTime, dateTimeAsc, dateTimeDesc, totalAsc, totalDesc);
        DefaultTableModel billModel = (DefaultTableModel) jtbBill.getModel();
        billModel.setRowCount(0);
        for (Bill temp : bills) {
            billModel.addRow(new Object[]{temp.getBillId(), temp.getCustomerName(), temp.getMobileNumber(), temp.getOrderDate(), temp.getOrderTime(), temp.getTotal()});
        }
    }

    public double getTotalRevenue() throws SQLException {
        String customerName = jtfCustomerName.getText();
        String mobileNumber = jtfMobileNumber.getText();
        String orderDate = "";
        if (!jtfOrderDate.getText().equals("Format: yyyy-mm-dd")) {
            orderDate = jtfOrderDate.getText();
        }
        String orderTime = "";
        if (!jtfOrderTime.getText().equals("Format: hh:mm:ss")) {
            orderTime = jtfOrderTime.getText();
        }
        List<Bill> bills = new BillDao().getAllBill(customerName, mobileNumber,
                orderDate, orderTime);
        double total = 0;
        for (Bill temp : bills) {
            total += temp.getTotal();
        }
        return total;
    }

